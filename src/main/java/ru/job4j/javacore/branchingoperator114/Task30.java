package ru.job4j.javacore.branchingoperator114;

public class Task30 {
    public static void numModify(int number) {
        if (number < 0) {
            System.out.println(number * -1);
        } else if (number > 0) {
            System.out.println(number * 0);
        } else {
            System.out.println(number);
        }
    }
}
